from django.urls import path

from app.views.post import(
    update_post,
    delete_post,
    feed,
    reels,
    search_posts,
    post_detail,
    create_post,
)


app_name = "post"

urlpatterns = [

    # -------------------------------------------------------
    # Feed
    # -------------------------------------------------------

    path(
        "",
        feed,
        name="feed",
    ),

    # -------------------------------------------------------
    # Create
    # -------------------------------------------------------

    path(
        "create/",
        create_post,
        name="create",
    ),

    # -------------------------------------------------------
    # Search
    # -------------------------------------------------------

    path(
        "search/",
        search_posts,
        name="search",
    ),

    # -------------------------------------------------------
    # Reels
    # -------------------------------------------------------

    path(
        "reels/",
        reels,
        name="reels",
    ),

    # -------------------------------------------------------
    # Detail
    # -------------------------------------------------------

    path(
        "<uuid:post_id>/",
        post_detail,
        name="detail",
    ),

    # -------------------------------------------------------
    # Update
    # -------------------------------------------------------

    path(
        "<uuid:post_id>/edit/",
        update_post,
        name="update",
    ),

    # -------------------------------------------------------
    # Delete
    # -------------------------------------------------------

    path(
        "<uuid:post_id>/delete/",
        delete_post,
        name="delete",
    ),
]